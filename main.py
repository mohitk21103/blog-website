# admin decorator
def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if current_user.id != 1:
            return abort(403)
        return f(*args, **kwargs)

    return decorated_function


# TODO: Use Werkzeug to hash the user's password when creating a new user.
@app.route('/register', methods=["POST", "GET"])
def register():
    r_form = RegisterForm()
    if r_form.validate_on_submit():
        check_email = r_form.email.data
        result = db.session.execute(db.select(User).where(User.email == check_email))
        is_email_exist = result.scalar()

        if is_email_exist:
            flash("Your are already Registered, login using Your email id")
            return redirect(url_for("login"))
        hash_and_salted_password = generate_password_hash(r_form.password.data, method='scrypt', salt_length=16)
        new_user = User(
            name=r_form.name.data,
            email=r_form.email.data,
            password=hash_and_salted_password
        )
        db.session.add(new_user)
        db.session.commit()
        login_user(new_user)
        return redirect(url_for("get_all_posts"))

    return render_template("register.html", form=r_form, current_user=current_user)


